            Umso höher die Lautstärke ist, desto grösser ist auch die Gefahr fürs menschliche Gehör.
            Sehr lauten Geräuschen sollte man nicht zu lange ausgesetzt sein, da das Gehör sonst Schaden nimmt.</p>
