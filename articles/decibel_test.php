            Der Gehörsinn ist der Sinn, der bei den meisten Menschen morgens als Erster aktiviert wird.
            Ohne einen Wecker schaffen es viele gar nicht erst aus dem Bett.</p>
        <p>Die Lautstärke von Geräuschen kann, zum Beispiel mit einer App, in Dezibel (dB) gemessen werden.
