<div class="container">
    <h1>Die Jugendmusik Glis am Eidgenössischen Jugendmusikfest in Burgdorf</h1>

    <img src="../src/burgdorf/group.jpg" class="title-picture rounded" alt="Gruppenfoto Jugendmusik Glis">

    <p>Kurz nach sechs Uhr morgens treffen die Jugendmusikanten im Musiklokal ein. Die meisten noch sehr
        verschlafen, andere bereits bestens gelaunt. Materialcheck, haben alle alles dabei?
        Noten, Deckblatt, Sichtmappe, Notenlyra, Uniform, Instrument, Dämpfer, Ersatzplättchen, Öl und Fett,
        schwarze Socken, schwarze Schuhe, Mundstück, Krawatte, neues Polo-Shirt? Jaja, meinen die meisten.
        Na dann mal los.
        Unser Bus wartet unten bei der Shell-Tankstelle auf uns. Wir verladen unser Material und steigen ein.
        In der Zwischenzeit sind auch unsere Gäste, welche mit dem Bus kommen, eingetroffen.
        Pünktlich um 7 Uhr fahren wir in Glis ab.</p>
    <p>Die 3-stündige Busfahrt über Vevey nach Burgdorf erlebt jeder etwas anders.
        Einige schlafen, andere hören Musik, wieder andere schauen einen Film und ein paar sehr Interessierte
        lesen die "FÄSCHTZITTIG".</p>
</div>

<div class="container">
    <img src="../src/burgdorf/zerzuben.jpg" class="img-fluid mobile-img" alt="Zerzuben Car">
