    <p>Die Jugendmusik liegt sich in den Armen und ich darf den Pokal für den Kategoriensieg auf der Bühne
        entgegennehmen.
        Ich bin richtig stolz auf unsere Jugend!
        Mit dem Pokal und dem Diplom mache ich mich auf den Weg zurück zu unseren Leuten.
        Aber halt, da kommt mir noch in den Sinn, dass ich nochmals auf die Bühne darf, um einen weiteren Pokal
        abzuholen.
        Denn bekanntermassen haben wir ja auch in der Marschmusik in unserer Kategorie den
        Schweizermeistertitel geholt.
        Mit beiden Pokalen in den Händen mache ich mich abermals auf den Weg zu unserer Jugendmusik.
        Die Pokale gehen in die Runde und es werden Fotos geschossen aus allen Richtungen, die Pokale mit Bier
        gefüllt und rund herumgegeben.
        Doppelschweizermeister Jugendmusik Glis.
        Nach gefühlt einer Stunde ist die erste Euphorie nur knapp am Abnehmen und wir merken, dass der Platz
        und das Festzelt nun schon ziemlich leer sind.
        Trotzdem lassen wir uns nicht davon abhalten, in Marschformation johlend ins Zelt einzumarschieren und
        Teile des Marignan zum Besten zu geben.</p>
    <p>Auch wir treten bald unsere Rückreise mit dem Car an.
        Unser Chauffeur Lothar hat diesen nun auch speziell mit Schweizermeister angeschrieben.
        Im Car wird weiter gefeiert und auch die Juryberichte analysiert.
        Interessanterweise wurden auch die Tubas positiv erwähnt. Tubas, Plural.
        Offensichtlich spiele ich für mehrere.</p>
    <p>In Glis angekommen steigen wir mit Pauken und Trompeten aus dem Car.
        Unsere Freunde und Eltern warten bereits am Strassenrand und wir marschieren mit dem Marsch der
        Grenadiere in Glis ein.
        Wir werden herzlich empfangen und irgendwie freuen sich doch alle, wieder zu Hause zu sein.</p>
    <p>Nach dem Aufräumen und Deponieren der Instrumente im Musiklokal geht es mit der Feier im Bäjipub noch
        weiter.
        Ein Dank hierbei an die MG Glishorn Glis, welche die Kosten für die Feier im Pub übernimmt.
        Als Vertreter der Politik richtet Daniel Studer einige Worte an die Jugendmusik.</p>
    <p>Wie lange noch gefeiert wird, entzieht sich meiner Kenntnis.
        Auch ich bin ausnahmsweise mal müde und gehe nicht als Letzter ins Bett.
        Zwei hervorragende Tage haben wir in den Kreisen der Jugendmusik in Burgdorf verbringen dürfen und
        ebenso zwei Titel bringen wir aus Burgdorf nach Hause.
        Ich bedanke mich recht herzlich bei allen Beteiligten, allen voran unseren fleissigen und
        hochmotivierten Jugendmusikantinnen und Jugendmusikanten, deren Eltern sowie meinem Vorstand und allen
        Unterstützern.
        Aufgrund der verspäteten Ausführung dürfen wir uns zwar nur 4 Jahre Doppelschweizermeister nennen,
        jedoch werden wir diese 4 Jahre umso mehr geniessen.</p>
    <p>Es lebe die Jugendmusik Glis!</p>
    <p>Präsident, Dominic Wenger</p>

