<div class="container">
    <p>Nun geht's wieder zurück ins Instrumentendepot. Es heisst Umziehen, Abendessen und Unterkunft beziehen.
        Die etwa zehnminütige Fahrt wird mit Walliser und Bündner Liedern überbrückt und die Chauffeuse lässt
        sich sogar auf #driimalumechreisel ein.
        In der Unterkunft, wo für uns aufblasbare Luftmatratzen zur Verfügung stehen, werden erst mal alle
        verfügbaren Turnmatten aus dem Geräteraum geholt und fair unter den beiden Musiken Glis und Giuventüna
        Engiadina Bassa verteilt.
        Für den sportlichen Teil - wir sind ja schliesslich in einer Turnhalle - spielen wir spontan Fussball.</p>
    <p>Den Abend lassen wir auf dem Festgelände mit gurer Musik, Unterhaltung und Bier/Mineral ausklingen.
        Pünktlich um 23:15 Uhr sind auch alle unter 16-Jährigen wieder beim Instrumentendepot und kehren mit
        doch recht vielen über 16-Jährigen zur Unterkunft zurück.</p>
    <p>Unser erster Termin am Sonntag ist das Frühstück.
        Im Gegensatz zu den Fassstrassen vom Mittag- und Abendesser ist dies en wenig chaotischer organisiert,
        da es keine fixen Esszeiten gibt.
        So stehen zum Zeitpunkt, wo wir essen wollen, geschätzt 1000 andere Jugendmusikantinnen und
        Jugendmusikanten an.
        Kurz bevor unsere Ersten ihr Frühstück fassen dürfen, will sich noch ein ganzes Korps vor uns drängeln.
        Auch hier zeigt sich wieder der Zusammenhalt unserer Jugendmusik.
        Einige stehen da, zwischen den "Feinden" und unseren eigenen Leuten, und bilden eine undurchdringbare
        Mauer.</p>
    <p>Sogleich geht es weiter mit dem Fototermin.
        Nach dem Fototermin gehen wir ruhig und gelassen Richtung Marschmusikstrecke.
        Auf einem grösseren Platz proben wir nochmal die wichtigsten Sachen wie Einstehen, Ausrichten, Start
        und Stopp und natürlich auch den Marsch.
        Als wir am Start der Marschmusikstrecke ankommen, heisst es erst mal, 30 Minuten Verspätung.</p>
    <p>Das grosse Warten beginnt. Die Spannung, welche vorhin aufgebaut worden ist, ist nun weg.
        Können wir uns wieder fassen und das liefern, was wir geübt haben?
        Vor unserem Start bemerkt Gorges noch einige Visper Militärtambouren und fragt ungeniert nach, ob sie
        auch die Möglichkeit haben, bei uns mitzumachen.
        Kurz darauf erhalten wir auch die Bestätigung. Cool, wir haben Tambouren für den Einmarsch.
        Das Warten scheint jedoch kein Ende zu nehmen.
        Nochmals wird ein Bus durch die Marschmusikstrecke gelassen und der Einmarsch verzögert sich weiter.
        Zum Glück wird in der Zwischenzeit Wasser für die wartenden Vereine angeliefert.</p>
    <p>Und nun geht's los. Wir stehen ein. Ich bin ein wenig nervös. Halten sich alle still?
        Ist die Instrumentenhaltung einheitlich? Ich sehe es aus der ersten Reihe ja nicht.
        Merkt die Jury, dass wir bei der Einleitung ein klein wenig tricksen? AAAACHTUNG! .......
        Anmeldung korrekt, die Jury geht durch die Reihen und schreibt.
        In der Ruhe hört man jeden Bleistiftschwund auf dem Papier. Tambourbeginn, Tambour vorwärts, Marsch.
        Tambouren statt Snare Drum macht schon einen gaaaaanz anderen ersten Eindruck. Spielwechsel.
        Es klingt gut, satt und präzise. Viel mehr kann ich dazu nicht sagen.
        Am Ende angekommen klappt zumindest in der ersten Reihe auch das Anhalten.
        Georges gibt das Kommando "Ruhn".
        Trotzdem bleiben alle in Spannung stehen und auch die Jurorin erwähnt, dass dies eigentlich ihr Job sei.
        Geschafft, nun sind alle Wettbewerbsteile durch.
        Wir warten noch gespannt auf das Resultat der Marschmusik, welche jeweils 2 Formationen später
        bekanntgegebn wird.
        Vom Vortag wissen wir, dass das beste Resultat 92 Punkte von der Brass Band Berner Oberland Junior war,
        welche bekanntermassen eine Spitzenformation ist.</p>
    <p>Und wieder kommt ein Bus, Resultate werden verzögert.
        Vor dem Start der Jugendmusik Brig wird unser Resultat verkündet.
        Dreiund....neunzig Punkte. Ein Jubel geht durch die Menge und alle freuen sich riesig über dieses Resultat.
        Am Bierstand am Ende der Marschstrecke werden mehrere Runden ausgegeben und das restliche Tagesprogramm
        mehr oder weniger ausser Kraft gesetzt.
        Es ist Zeit zu feiern! 93 Punkte, das heisst bisher Bestresultat in unserer Kategorie!
        Und es kommen nur noch 2 weitere Formationen in der gleichen Kategorie.
        Georges versichert mir, dass wir von denen nichts zu befürchten haben.
        Kurze Zeit später wird uns dies auch bestätigt.</p>
    <p>Zur Rangverkündung trifft sich die Jugendmusik Glis in der Mitte des Festzelts.
        Es werden Reden gehalten von diversen Vertretern der Politik, des Vorstandes Schweizer
        Jugendmusikverband und dem OK.
        Doch wir alle warten gespannt auf die Rangverkündung, vor allem natürlich auf die Resultate des
        Konzertvortrags.
        Kategorienweise geht es von unten nach oben. Nun ist auch die Kategorie Harmonie Oberstufe an der Reihe.
        Ich darf auf die Bühne.
        Bereits auf Rang 6 werden Punktzahlen über 90 Punkte verkündet, eine starke Kategorie.
        Dass es uns recht gut ergangen ist, wussten wir alle. Aber wie gut, das war die Frage.</p>
    <p>Platz 3 mit 92.83 Punkten, Jugendmusik... Belalp Naters.
        Ich gratuliere David Lochmatter zum Podestplatz und er holt seinen Preis ab.
        Platz 2 mit 93.00 Punkten, Jugendmusik... Zürich 11, die Jugendmusik meines ehemaligen Kommandanten
        im Militär, Bernhard Meier.
        Nun gibt's nur noch alles oder nichts.
        Die Jugendmusik Naters hat bereits am Kantonalen Musikfest bewiesen, dass sie nicht zu unterschätzen ist
        und sie sind am Schweizer Jugendmusikfest mit denselben Stücken angetreten.
        Schweizermeister der Kategorie Harmonie Oberstufe mit 94.00 Punkten ist... 3 Sekunden absolute Stille...
        Jugendmusik Glis!</p>
</div>

