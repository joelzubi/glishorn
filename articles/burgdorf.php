<div class="container">
    <p>So pünktlich wie wir abgefahren sind, so pünktlich kommen wir auch endlich in Burgdorf an.
        Zunächst heisst es, alles aus dem Bus ausladen und zum Instrumentendepot bringen.
        Bis hierhin ist ja mal alles bestens verlaufen.
        Na gut, es hat ja auch noch niemand etwas auspacken müssen.
        Wir sind immer noch beim gemütlichen Teil in Burgdorf.
        Wir verteilen die Festabzeichen und die Essensgutscheine für das Mittagessen.
        Die Fassstrassen sind gut organisiert und wir können ohne Anstehen unser Essen holen und geniessen.</p>
    <p>Nach dem Essen haben wir uns im Instrumentendepot umgezogen und bereit gemacht für den ersten Ernstfall
        des Wochenendes - die Konzertmusik.
        Kurz zwei oder drei Krawatten verteilt und schon konnte es losgehen.
        Ein kleiner Fussmarsch zum Einspiellokal und wir sind bereit, uns aufs Wesentliche zu konzentrieren.
        Im Wissen, was auf sie zukommt, lassen sich die Jugendmusikanten auch nicht davon ablenken, dass ein
        Euphonium plötzlich nicht mehr funktioniert.
        Es kommt einfach kein Ton mehr raus.
        Wir müssen schnell handeln, es bleibt nicht mehr allzu lange Zeit, da stehen wir schon auf der Bühne.
        Die nette Helferin vor Ort zeigt uns, wo sich der Instrumentenreparateur auf dem Festgelände befindet.
        Also schicke ich abermals meinen Vize mitsamt Romaine dorthin.</p>
    <p>Zurück im Einspiellokal stehen plötzlich mehrere Personen auf und beichten mir, dass sie die ID vergessen
        haben.
        Die eine konnten wir als Foto organisieren und die andere war zum Glück nur im Instrumentendepot im
        Veston der Uniform.
        Da Lukas bereits unterwegs war, konnte er diese auch gleich holen.
        Durch die Reihen probiert Isabelle, mit mir ein paar Atemübungen zur Beruhigung zu machen.
        Dies funktioniert auch gar nicht mal so schlecht.</p>
    <p>15:25: Die Ersten können nun bereits ins Konzertlokal, um die Stühle, Notenständer und das
        Schlagzeugmaterial einzurichten. Die Anderen stossen erst kurz vor Vortragsbeginn dazu.
        Wir richten uns ein und lauschen konzentriert der Stille im Saal. Jetzt wird's ernst.
        Es ist unbeschreiblich, was auf der Bühne mit unserer Jugendmusik passiert. Vollste Konzentration.
        Es ist in den eigenen Rängen sogar noch stiller als an unseren Konzerten.</p>
    <p>Zweimal zehn Minuten, und wir wissen danach, dass wir die beiden Stücke "The Revenge of the Witch" und
        "Rikudim" wohl noch nie so gut gespielt haben.
        Aber die Konkurrenz schläft nicht.
        Was unser Vortrag wirklich wert ist, erfahren wir erst am Sonntag.</p>
</div>

