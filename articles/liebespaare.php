        <p>Seit dem Sonntag, dem 1.&nbsp;Dezember&nbsp;2019, war es uns aus wohlbekannten Gründen nicht mehr vergönnt,
            mit einem Konzert vor Sie, wertes Publikum, zu treten.
            Ganze 735&nbsp;Tage lang wurde es still, und es war uns nicht mehr möglich und erlaubt, vor einem grossen
            Publikum zu zeigen, was wir in langen Proben erarbeitet hatten.
            Umso mehr freut es uns, Sie in diesem Jahr hoffentlich wieder zahlreich am
            <time datetime="2021-12-05 17:00">5.&nbsp;Dezember um 17:00&nbsp;Uhr</time>
            in der Wallfahrtskirche in Glis begrüssen zu dürfen.</p>
        <p>Das diesjährige Konzert steht unter dem Titel &laquo;Liebespaare&raquo;, das heisst, im Zentrum stehen
            musikalisch aufgearbeitete Liebesgeschichten.
            Von klassischen, wie derjenigen von Elsa von Brabant und Lohengrin aus der gleichnamigen Oper Richard
            Wagners, zu biblischen, wie derjenigen des starken Samson, dessen in seinen Haaren steckende
            übermenschliche Kraft ihm von seiner geliebten Dalilah geraubt wird, indem sie ihn schert.</p>
        <p>Kleists Drama &laquo;Das Käthchen von Heilbronn&raquo;
            diente dem belgischen Komponisten Bert Appermont als Vorlage für sein Werk
            <cite>Battle of Hearts</cite>,
            welches einerseits die beiden Opponentinnen um das Herz des Grafen von
            Stahl, Katharina und Kunigunde von Thurneck musikalisch beschreibt, und Katharina/Käthchen schliesslich
            nach bestandener Feuerprobe ein Happyend (auch musikalisch) als die verlorene, uneheliche Tochter des
            Kaisers beschert.</p>
        <p>Stilistisch ganz anders kommt James Barnes' Arrangement von George Gershwins Oper
            <cite>Porgy and Bess</cite> daher.
            1935 uraufgeführt, als erste Oper, deren Protagonisten ausschliesslich Afroamerikaner waren, orientiert
            sich die Musik sehr stark an jazzigen Elementen, etwas, was auch sonst stark in Gershwins Werk anklingt.</p>
        <p>Abgerundet wird das Konzert durch das
            <cite>Lento</cite>
            aus Philip Sparkes
            <cite>Dance Movements</cite>,
            einem nur für Blechbläser und Perkussion geschriebenen Satz, in dessen Zentrum vor allem das
            Atmosphärische steht.</p>
        <p>Alles in allem ein Programm, für das es sich lohnt, am
            <time datetime="2021-12-05 17:00">5.&nbsp;Dezember</time>
            in die Wallfahrtskirche&nbsp;Glis zu kommen.
            In diesem Sinne wünschen wir, alle Musikantinnen und Musikanten der Musikgesellschaft Glishorn Glis,
            Ihnen einen besinnlichen Advent und im Voraus frohe Weihnachten.</p>
